#    1. If-Else Construct

"""
If <expression>:
     statements
else:
     statements
"""


#With if-elif-else:
"""
if <expression>:
    statements
elif <expression>:
    statements
else:
    statements
"""

#Example of a basic program:
"""
A = 10
B = 5
if A > B:
   print("A is larger number")
else:
   print("B is larger number")
"""



#    2. While Construct
"""
while <expression>:
   statements
while i<10:
   print(i)
   i = i + 1
"""

# While-Else example:
"""
i = 1
while i < 10:
   print(i)
   i = i + 1
else:
   print(i,"- i is no longer smaller than 10")
"""

